# Teoria dos Sentimentos no Trade

Este documento explora como os sentimentos podem influenciar as decisões de um trader e quais indicadores podem ajudar a identificar esses sentimentos de forma previsível. Abaixo está uma lista de sentimentos comuns e os indicadores que podem estar associados a eles, organizados em duas categorias: Sentimentos de Compra e Sentimentos de Venda.

## Sentimentos de Compra

### 1. Vontade
- **Indicadores Possíveis:**
  - OBV (On-Balance Volume) indicando acumulação
  - Cruzamento de Médias Móveis (indicando entrada de força compradora)
  - Padrões de Price Action (como rompimentos de resistência)

### 2. Confiança
- **Indicadores Possíveis:**
  - ADX (Average Directional Index) indicando força de tendência
  - Médias Móveis Exponenciais (EMA) alinhadas em direção à tendência
  - Volume em Aumento Consistente

### 3. Euforia
- **Indicadores Possíveis:**
  - RSI (Índice de Força Relativa) em Sobrecompra
  - Sequência de Candles de Alta sem Correção
  - Expansão das Bandas de Bollinger

### 4. Esperança
- **Indicadores Possíveis:**
  - Divergência no IFR (Índice de Força Relativa)
  - Redução no Volume de Venda
  - Candles de Reversão (como Martelo)

## Sentimentos de Venda

### 1. Rejeição
- **Indicadores Possíveis:**
  - Volume Anormal (indicando rejeição de preços em níveis específicos)
  - Candles de Reversão (como Doji ou Martelo Invertido)
  - Bandas de Bollinger (preço tocando ou ultrapassando as bandas externas)

### 2. Cansaço
- **Indicadores Possíveis:**
  - Divergência no IFR (Índice de Força Relativa)
  - Redução no Volume de Negociação
  - ATR (Average True Range) mostrando diminuição na volatilidade

### 3. Medo
- **Indicadores Possíveis:**
  - Índice de Volatilidade (VIX) em alta
  - Candles de Forte Venda (como Marubozu de baixa)
  - Aumento no Volume em Movimentos de Queda

### 4. Frustração
- **Indicadores Possíveis:**
  - Stop Loss Frequente em Movimentos Laterais
  - Falha em Rompimentos (falsos rompimentos)
  - RSI (Índice de Força Relativa) em zonas de sobrecompra ou sobrevenda sem continuidade

### 5. Desespero
- **Indicadores Possíveis:**
  - Aumento Repentino no Volume de Venda
  - Candles com Sombras Longas de Baixa
  - Queda Acentuada no Preço sem Suporte Visível

## Conclusão

Os sentimentos no trade podem ser difíceis de controlar, mas entender os indicadores que refletem esses sentimentos pode ajudar os traders a tomarem decisões mais racionais e menos emocionais. Este documento serve como um guia inicial para identificar padrões de comportamento emocional no mercado.